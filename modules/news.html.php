    <h2>TCG National Conference: Game Change 2015</h2>
    <div class="post">
        June 20, 2015: 12:15PM-1:30PM
        <br>
        Special Lunchtime Conversation
        <br>
        Blue Star Theatres – Theatre Techniques for Veterans
        <br>
        Speakers: Everett Cox, Alex Mallory, KJ Sanchez, Paula Vogel, and Stephan Wolfert
        <br><br>
        The livestreamed conversation is available at <a href="http://howlround.com/livestreaming-the-25th-annual-theatre-communications-group-national-conference-in-cleveland-thurs 
" target="_blank">http://howlround.com/tv</a> (scroll down, it's the third to last video!)
        <br><br>
        <p>Have you wanted to engage with active-duty military personnel or Veterans and their families? Don’t know how to get connected to the military community near your theatre? Join this lunchtime conversation and learn about the work of the extraordinary Artists and Veterans on this panel. They will provide tips on how to talk to someone in uniform and on producing work by and for veterans. This conversation will ignite your interest and passion in connecting with military families.</p>
        <p>TCG’s Blue Star Theatres program is intended to recognize the contributions of service families, to build stronger connections between the theatre community and military families in communities all across the country and to help in whatever small way we can to aid service people and their families as they seek to be integrated into the lives of their communities.</p>
